package ch01_start;

public class HelloWolrd {

    // main 메소드 (main 치고 자동완성)
    public static void main(String[] args) {
        // 코드실행 [Shift + F10]
        // main 메소드 내에 존재하는 코드가 실행된다.
        
        // 코드 라인 복사 [Ctrl + D]
        System.out.println("Hello World!!");
        System.out.println("World!!");
        
        // 코드 라인 삭제 [Ctrl + Y]
        // 실행 취소 [Ctrl + Z]
        // 실행 취소의 취소 [Ctrl + Shift + Z]
        System.out.println("World!!");

        // 자동완성 [Ctrl + Space]
        System.out.println();
        System.out.println();

        // so 입력 후 엔터
        System.out.println();
        System.out.println();

    }

}